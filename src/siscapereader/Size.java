public class Size {

    int xInit;
    int yInit;
    int xEnd;
    int yEnd;

    public int getxEnd() {
        return xEnd;
    }

    public void setxEnd(int xEnd) {
        this.xEnd = xEnd;
    }

    public int getxInit() {
        return xInit;
    }

    public void setxInit(int xInit) {
        this.xInit = xInit;
    }

    public int getyEnd() {
        return yEnd;
    }

    public void setyEnd(int yEnd) {
        this.yEnd = yEnd;
    }

    public int getyInit() {
        return yInit;
    }

    public void setyInit(int yInit) {
        this.yInit = yInit;
    }
}
